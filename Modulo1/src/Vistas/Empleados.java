    private void deleteEmpleado() {
        if ($empleados.deleteEmpleado(getEmpleado)) {
            resetVariables();
            JOptionPane.showMessageDialog(null, "Se elimino el empleado");
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrio un error");
        }
    }

    private void guardarEmpleado() {
        Empleado empleado = new Empleado();
        empleado.setId_persona(getEmpleado.getId_persona());
        empleado.setAmaterno(txtAmaterno.getText());
        empleado.setApaterno(txtApaterno.getText());
        empleado.setCargo(String.valueOf(jcbCargo.getSelectedItem()));
        empleado.setContrasenia(txtContraseña.getText());
        empleado.setDireccion(txtDireccion.getText());
        empleado.setEdad(Integer.parseInt(txtEdad.getText()));
        empleado.setEmail(txtEmail.getText());
        empleado.setNombre(txtNombre.getText());
        empleado.setNombreUsuario(txtUsuario.getText());
        empleado.setTelefono(txtTelefono.getText());
        empleado.setFoto(nameImage);
        if ($empleados.updateEmpleado(empleado)) {
            resetVariables();
            JOptionPane.showMessageDialog(null, "Actualizacion Exitosa");
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrio un error");
        }
    }

